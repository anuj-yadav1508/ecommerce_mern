class Product {
    constructor(id, ownerId, title, imageUrl, description, price, color, category) {
        this.id = id;
        this.ownerId = ownerId;
        this.title = title;
        this.imageUrl = imageUrl;
        this.description = description;
        this.price = price;
        this.color = color;
        this.category = category;
    }
};


export default Product;